#include "ft_scmd.h"

void	ft_scmd_print(t_scmd *tail)
{
	t_scmd	*head;

	head = tail;
	while (head)
	{
		printf("====================== SCMD ======================\n");
		ft_token_print(head->tokens);
		head = head->next;
		printf("==================================================\n");
	}
}
